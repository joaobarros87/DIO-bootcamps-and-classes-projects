package dio.com.br;

public class methodCalc {
    public static void sum(double number1, double number2) {

        double result = number1 + number2;

        System.out.println("The result of " +  number1 + " plus " +  number2 + " is: " + result);

    }

    public static void subtract(double number1, double number2){

        double result = number1 - number2;

        System.out.println("The result of " +  number1 + " minus " +  number2 + " is: " + result);
    }

    public static void multiply(double number1, double number2){

        double result = number1 * number2;

        System.out.println("The result of " +  number1 + " times " +  number2 + " is: " + result);
    }

    public  static void divide(double number1, double number2){

        double result = number1 / number2;

        System.out.println("The result of " +  number1 + " divided by " +  number2 + " is: " + result);
    }






}
